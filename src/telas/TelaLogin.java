    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        String username = txtUsuario.getText();
    String senha = new String(txtSenha.getPassword());
    try (Connection conn = Conexao.getConexao();
         PreparedStatement ps = conn.prepareStatement("SELECT * FROM usuarios WHERE username=? AND senha=?")) {
        ps.setString(1, username);
        ps.setString(2, senha);  
        ResultSet rs = ps.executeQuery();
        if (rs.next()) {
            
            UserSession.idFuncionario = rs.getInt("id_funcionario");
            UserSession.permissoes = rs.getString("permissoes");
            
            new TelaPrincipal().setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Inválido!");
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage());
    }
    }//GEN-LAST:event_btnEntrarActionPerformed

